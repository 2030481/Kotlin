/*
* a++ increment operator a=a+1
* a-- decrement operator a=a-1
*/
fun main(){
var number1 = 10
    var number2 = 5
	
    number1++
    println(number1)
    
    number2--
    println(number2)
    
    /*another method to do the same*/
    
    ++number1
    println(number1)
    
    --number2
    println(number2)

}
